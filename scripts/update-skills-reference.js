#!/usr/bin/env node
/**
 * update-skills-reference.js
 * Reads all .claude/commands/*.md files and regenerates SKILLS.md at the repo root.
 * Runs automatically after any Write/Edit to .claude/commands/, or manually:
 *   node scripts/update-skills-reference.js
 */

const fs   = require('fs');
const path = require('path');

const REPO_ROOT    = path.join(__dirname, '..');
const COMMANDS_DIR = path.join(REPO_ROOT, '.claude', 'commands');
const OUTPUT_FILE  = path.join(REPO_ROOT, 'SKILLS.md');

// When triggered by a hook, only regenerate if the edited file was a command file
if (process.env.CLAUDE_TOOL_INPUT) {
  try {
    const input    = JSON.parse(process.env.CLAUDE_TOOL_INPUT);
    const filePath = (input.file_path || input.path || '').replace(/\\/g, '/');
    if (!filePath.includes('.claude/commands')) process.exit(0);
  } catch { process.exit(0); }
}

if (!fs.existsSync(COMMANDS_DIR)) {
  console.log('[update-skills-reference] No .claude/commands/ directory found — skipping.');
  process.exit(0);
}

// ── Hardcoded examples (update when adding a skill that takes arguments) ──
const EXAMPLES = {
  'health':         '/health',
  'run-tests':      '/run-tests smoke',
  'add-regression': '/add-regression data-href links not captured',
  'check-fragile':  '/check-fragile',
  'save':           '/save fixed link capture bug',
  'new-test':       '/new-test login flow',
};

// ── Parse each command file ────────────────────────────────────────────────
const skills = fs.readdirSync(COMMANDS_DIR)
  .filter(f => f.endsWith('.md'))
  .sort()
  .map(file => {
    const name    = file.replace('.md', '');
    const content = fs.readFileSync(path.join(COMMANDS_DIR, file), 'utf8').trim();
    const lines   = content.split('\n');

    // First non-empty line = short description
    const description = lines.find(l => l.trim())?.trim() || '';
    const hasArgs     = content.includes('$ARGUMENTS');
    const example     = EXAMPLES[name] || (hasArgs ? `/${name} [description]` : `/${name}`);

    return { name, description, example, hasArgs };
  });

// ── Build SKILLS.md ───────────────────────────────────────────────────────
const date = new Date().toISOString().slice(0, 10);

const sections = skills.map(s => [
  `### \`/${s.name}\``,
  '',
  `> ${s.description}`,
  '',
  `**Example:** \`${s.example}\``,
  `**Arguments:** ${s.hasArgs ? 'Yes' : 'No'}`,
  `**Source:** [\`.claude/commands/${s.name}.md\`](.claude/commands/${s.name}.md)`,
  '',
].join('\n'));

const quickRef = skills
  .map(s => `| \`${s.example}\` | ${s.description.replace(/\*\*/g, '').slice(0, 80)} |`)
  .join('\n');

const output = `# Skills Reference

All available slash commands for this project.

> ⚠️ **Auto-generated — do not edit this file directly.**
> To change a skill, edit its \`.claude/commands/[name].md\` file.
> To add a skill, create a new \`.claude/commands/[name].md\` — this file will regenerate automatically.

_Last updated: ${date} · ${skills.length} skills_

---

## Quick Reference

| Example | Description |
|---------|-------------|
${quickRef}

---

## Full Details

${sections.join('\n')}---

_Add a new skill → create \`.claude/commands/[skill-name].md\` → this file auto-updates._
`;

fs.writeFileSync(OUTPUT_FILE, output, 'utf8');
console.log(`[update-skills-reference] SKILLS.md updated — ${skills.length} skills listed.`);
